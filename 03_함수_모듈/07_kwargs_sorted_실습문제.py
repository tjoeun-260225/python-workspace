def 할인가격(가격, 할인율):
    return 가격 * (1 - 할인율)


# lambda 로 바꾸기
#          여기서만
#          사용되는
#          기능이다  매개변수들 : return 결과
할인가격 = lambda 가격, 할인율: 가격 * (1 - 할인율)

print(할인가격(10000, 0.1))  # → 9000.0


def 글자수세기(문장):
    return len(문장)


# lambda 로 바꾸기
글자수세기 = lambda 문장: len(문장)

print(글자수세기("안녕하세요"))  # → 5

거래내역 = [
    {"내용": "편의점", "금액": 3500},
    {"내용": "카페", "금액": 6000},
    {"내용": "마트", "금액": 45000},
    {"내용": "택시", "금액": 12000},
]

# lambda 로 금액 기준 내림차순 정렬
결과 = sorted(거래내역, key=lambda x: x["금액"], reverse=True)

for 내역 in 결과:
    print(내역["내용"], 내역["금액"])

