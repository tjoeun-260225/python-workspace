def 배달주문(가게이름, *메뉴들):
    print(f"{가게이름}에서 {메뉴들} 주문")


배달주문("맥도날드", "빅맥", "콜라", "감자튀김")


def 토스송금(*받는사람들, 총금액):
    인당 = 총금액 // len(받는사람들)
    for 사람 in 받는사람들:
        print(f"{사람}: {인당}원")


토스송금("철수", "영희", "민준", 총금액=30000)


def 카카오단체전송(*받는사람들, 메세지, 타입="text"):
    for 사람 in 받는사람들:
        print(f"{사람}에게 {타입}로 {메세지} 전송")


카카오단체전송("철수", "영희", 메세지="안녕하세요", 타입="image")


def 네이버쇼핑(*상품들, 쿠폰금액):
    print(f"쿠폰 {쿠폰금액}원 적용")
    for 상품 in 상품들:
        print(f"상품: {상품}")


네이버쇼핑("노트북", "마우스", "키보드", 쿠폰금액=5000)


def 구글캘린더(*참여자들, 제목, 날짜="미정"):
    print(f"일정: {제목} / 날짜: {날짜}")
    for 참여자 in 참여자들:
        print(f"{참여자} 초대됨")


구글캘린더("철수", "영희", "민준", 제목="팀회의", 날짜="2025-05-01")
