'''
lambda = 수학/논리학
함수를 표현하는 수학적 표기법 을 만들었다.
入계산법(Lambda Calculus) 라고 부르게됨
入x. x* 2 = x를 받아서 x*2 를 돌려주는 함수(=기능)

알론조 처치의 入(lambda) 계산법 -> 1958 프로그래밍 도입
-> python, java, javaScript 등 대부분의 언어가 그대로 알론조 처치의 수학법 가져와서 프로그래밍으로 사용

수학자가 인쇄 문제로 그리스 문자 入 를  사용했고, 그것을 프로그래밍 언어에서 사용하는 것!
'''


def 두배(x):
    return x * 2


두배 = lambda x: x * 2

print(두배(5))  # → 10


def 세금붙이기(가격):
    return 가격 * 1.1


# lambda 로 바꾸기
세금붙이기 = lambda 가격: 가격 * 1.1

print(세금붙이기(10000))  # → 11000.0


