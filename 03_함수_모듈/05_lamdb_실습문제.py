뉴스 = [
    {"제목": "AI 혁신", "조회수": 82000},
    {"제목": "경제 동향", "조회수": 45000},
    {"제목": "스포츠", "조회수": 120000},
]

# lambda 로 조회수 기준 내림차순 정렬
# sorted 오름차순  sorted(reverse=True) -> 오름차순 거꾸로 뒤집어서 정렬하기 = 내림차순
결과 = sorted(뉴스, key=lambda x: ["조회수"], reverse=True)

for 기사 in 결과:
    print(기사["제목"], 기사["조회수"])

