숫자들 = [3, 1, 1, 3, 4, 5]
이름들 = ["철수", "영희", "민준"]

print(sorted(숫자들))  # [1, 1, 3, 3, 4, 5]
print(sorted(이름들))  # ['민준', '영희', '철수']
print(sorted(숫자들, reverse=True))  # [5, 4, 3, 3, 1, 1]
# 비교할 데이터가 단순해서 key가 없어도 된다. 정렬할 기준이 안에 있는 데이터들 뿐이기 때문

# 이름 길이순으로 정렬하고 싶다 ㄱ ㄴ ㄷ 순서가 아니라 이름길이로 정렬하고 싶다면


이름들 = ["박철수", "안영희야", "박민준이야"]

print(sorted(이름들, key=lambda x: len(x)))


def 길이순서기능(x ):
    return len(x)

순서변경 = sorted(이름들, key=길이순서기능)
print(순서변경)
