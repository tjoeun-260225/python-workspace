    두배 = 조회수 * 2
    print(두배)
    # todo_3: 위 두 배열을 더한 결과를 출력하세요
    print(천단위 + 두배)
    # todo_4: 위 두 배열을 뺀 결과를 출력하세요
    print(두배 - 천단위)


