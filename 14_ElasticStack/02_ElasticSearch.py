#### Elasticsearch 기초 개념 정리
"""
1. 인덱스(Index)
- RDB 의 테이블과 같은 개념 문서들의 묶음
- 인덱스를 만들 때 아무 설정 안 하면 ES 가 알아서 타입 추측
  하지만 잘못 추측하는 경우가 있어서 매핑으로 인덱스가 무엇인지 직접적으로 작성하는 안전
"""
from elasticsearch import Elasticsearch

# 도커로 실행한 엘라스틱서치 연결하여 연결결과를 es 변수 공간에 담아두기
es = Elasticsearch("http://localhost:9200")

# 인덱스 생성
es.indices.create(index="테이블이름")
# 인덱스 삭제
es.indices.delete(index="테이블이름")
# 인덱스 존재 여부 확인
es.indices.exists(index="테이블이름")

"""
2. 매핑(Mapping)
- 각 필드의 타입을 미리 정의하는 것
- RDB 스키마와 같다
- 타입별 차이 정리
text     →  형태소 분석O, 문장/긴 글에 사용 예) 상품명, 설명
keyword  →  형태소 분석X, 정확히 일치할 때  예) 카테고리, 브랜드
integer  →  정수                            예) 가격, 재고 
float    →  소수점 숫자                     예) 평점
date     →  날짜                            예) 등록일
"""
es.indices.create(index="테이블이름", body={
    "mappings": {
        "properties": {
            "컬럼1": {"type": "text"},  # 형태소 분석 O → match 검색용
            "컬럼2": {"type": "keyword"},  # 형태소 분석 X → term  검색용 단어나 문장이 100% 일치
            "컬럼3": {"type": "integer"},  # 숫자
            "컬럼4": {"type": "float"},  # 소수점 숫자
            "컬럼5": {"type": "date"},  # 날짜
        }
    }
})
"""
3. 쿼리 DSL 기초
- ES 에서 검색할 때 사용하는 JSON 형식의 검색 문법
- match     : 텍스트 검색 (형태소 분석 O)
- term      : 100% 정확히 일치하는 텍스트만 검색 (형태소 분석 X)
- range     : 숫자 범위 검색
- match all : 전체 조회
"""
# match - 텍스트 검색 (형태소 분석 O)
# 컬럼1 에 검색할 단어가 포함된 문서 검색
{
    "query": {
        "match": {
            "컬럼1": {"검색할 단어"},
            # "검색하다" "단어" 로 쪼개서 하나라도 포함되면 검색
        }
    }
}
# term - 정확히 일치 검색 (형태소 분석 X)
# 컬럼1 에 검색할 단어가 100% 일치하는 단어만 검색
{
    "query": {
        "term": {
            "컬럼1": {"검색할 단어"},
            # "검색할 단어" 로 100% 일치하는 단어만 검색된다.
        }
    }
}
# range - 범위검색
# 컬럼1의 숫자 범위 검색
{
    "query": {
        "range": {
            "컬럼1": {
                "gte": 10000,  # gte = 이상 (greater than or equal)
                "lte": 30000,  # lte = 이하 (less   than or equal)
            },
            # 컬럼1 에서 10000 이상 30000 이하
        }
    }
}
# 비교 연산자 정리
# gte → >= 이상
# lte → <= 이하
# gt  → >  초과
# lt  → <  미만


# match_all - 전체 조회
# 조건 없이 문서 전체 조회
{
    "query": {
        "match_all": {}
    }
}
