    # 파일 인코딩에 주의할 것
    df = pd.read_csv("경찰청_범죄 발생 지역별 통계_20241231.csv", encoding="cp949")
    # TODO: 상위 5개의 행을 출력하시오.
    print("상위 5개의 행 : ", df.head())

    # TODO: 행과 열의 개수를 출력하시오.
    print("행과 열의 개수 : ", df.shape)

    # TODO: 데이터 타입과 결측치 정보를 출력하시오.
    df.info()  # info() 기능 맨 아래에 프린트 출력이 내장되어 있어 print 를 작성하지 않아도 된다.
    print("결측치 정보 출력 : ", df.info())

    # TODO: 숫자 컬럼의 평균, 최소, 최대 등 통계 요약 정보를 출력하시오.
    print("통계 요약 : ", df.describe())

    # TODO: '범죄대분류', '범죄중분류', '서울 강남구', '서울 종로구' 4개 컬럼만 가져와 출력하시오.
    df_선택 = df[['범죄대분류', '범죄중분류', '서울 강남구', '서울 종로구']]
    print(df_선택)

    # TODO: 범죄대분류가 '강력범죄' 인 행만 필터링하여 출력하시오.
    df_강력범죄 = df[df['범죄대분류'] == '강력범죄']
    print("강력범죄 목록 : ", df_강력범죄)

    # TODO: 서울 강남구 범죄 발생 건수가 50건 이상인 행만 필터링하여 출력하시오.
    df_강남구_50이상 = df[df['서울 강남구'] >= 50]
    print("강남구 50건 이상 범죄 : ", df_강남구_50이상)

    # TODO: 위 결과가 총 몇 개의 행인지 출력하시오.
    print("총 행 수 : ", len(df_강남구_50이상))

    # TODO: '서울 강남구' 컬럼의 전체 합계를 출력하시오. (강남구 전체 범죄 건수)
    print("강남구 전체 범죄 건수 : ", df['서울 강남구'].sum())

    # TODO: '서울 강남구' 컬럼의 평균값을 출력하시오.
    print("강남구 범죄 평균 : ", df['서울 강남구'].mean())

    # TODO: '서울 강남구' 컬럼의 최댓값을 출력하시오.
    print("강남구 최다 범죄 건수 : ", df['서울 강남구'].max())

    # TODO: '범죄대분류', '범죄중분류', '서울 강남구', '부산 중구', '대구 중구' 컬럼만 추출하여
    # "범죄통계_정제완료.csv" 로 저장하시오.
    # 인덱스는 저장하지 않으며, 한글이 깨지지 않게 저장할 것
    df[['범죄대분류', '범죄중분류', '서울 강남구', '부산 중구', '대구 중구']].to_csv("범죄통계_정제완료.csv", index=False, encoding='utf-8-sig')
