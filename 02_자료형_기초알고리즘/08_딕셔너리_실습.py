def 평균점수계산():
    데이터 = [
        {"이름": "철수", "점수": 85, "과목": "수학"},
        {"이름": "영희", "점수": 92, "과목": "영어"},
        {"이름": "민수", "점수": 78, "과목": "과학"},
        {"이름": "지수", "점수": 88, "과목": "국어"},
    ]

    점수목록 = [행['점수'] for 행 in 데이터]
    평균 = sum(점수목록) / len(점수목록)
    print(f"전체 평균: {평균}점")

    # 전체 평균: 85.75점


