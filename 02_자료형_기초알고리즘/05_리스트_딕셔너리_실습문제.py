def 문제9():
    카카오_이모티콘 = {"라이언": 3500, "어피치": 2900, "무지": 4200, "콘": 1800}
    # dict comprehension 으로
    인기이모티콘 = {키: 값 for 키, 값 in 카카오_이모티콘.items() if 값 >= 3000}
    print(인기이모티콘)
    # 3000원 이상 이모티콘만 필터해서 출력


문제9()


# {'라이언': 3500, '무지': 4200}

def 문제10():
    # 인스타 유저 정보
    유저정보 = {
        "아이디": "hong_gil",
        "팔로워": 1500,
        "팔로잉": 300,
        "게시물수": 42
    }
    내팔로워 = {"유저A", "유저B", "유저C", "유저D"}
    내팔로잉 = {"유저A", "유저C", "유저E"}

    # 유저정보 전체 for문으로 출력
    for 키, 값 in 유저정보.items():
        print(f"{키} : {값}")
    print("맞팔 : ", 내팔로워 & 내팔로잉)
    print("언팔 : ", 내팔로잉 - 내팔로워)
    유저정보["팔로워"] = 2000
    print("수정된 팔로워 : ", 유저정보["팔로워"])
    # 팔로워 2000으로 수정 후 출력


문제10()
