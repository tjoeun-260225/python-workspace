def 문제3():
    인스타_좋아요 = [15, 320, 87, 1500, 43, 980, 2100]
    for i in 인스타_좋아요:
        if i >= 100:
            print(i)
    # list comprehension 으로
    # 좋아요 100개 이상인 것만 필터해서 출력
    백이상 = [i for i in 인스타_좋아요 if i >= 100]
    print(백이상)


문제3()


# [320, 1500, 980, 2100]

