def 문제8():
    네이버_쇼핑 = {
        "에어팟": 290000,
        "맥북": 1500000,
        "아이폰": 1200000,
        "애플워치": 550000
    }
    # for문으로 전체 출력
    for 상품, 가격 in 네이버_쇼핑.items():
        print(f"{상품} : {가격}원")

    # 100만원 이상 상품만 출력
    상품들 = {상품: 가격 for 상품, 가격 in 네이버_쇼핑.items() if 가격 >= 1000000}
    print(상품들)


문제8()


# 에어팟: 290000원
# 맥북: 1500000원
# 아이폰: 1200000원
# 애플워치: 550000원
# ===100만원 이상===
