'''
List : 리스트 순서가 있고 수정 가능한 목록이며 0번부터 시작 ArrayList 유사
작성법 : [] 안에 , 로 구분하여 0번 부터 순서가 부여
'''
# 구조 공식
# [저장할 값   for 변수 in 목록 if 조건]
def 리스트기초():
    # 1. 인덱싱 - 특정 위치 하나 꺼내기 0 ~ 순차적인 순서는 맨 앞에서부터 번호대로 사용
    #                              -1 ~ - 계열은 뒤에서부터 첫번째로 순번 구분을 해서 하나 꺼내야 할 때
    #                   0            1        2       3      4        5
    #                   0           -5       -4      -3     -2       -1
    플레이리스트 = ["사랑노래", "축제노래", "팝송", "R&B", "힙합", "발라드"]
    print(플레이리스트[0])
    print(플레이리스트[-1])
    print(플레이리스트[1])

    # 2. 슬라이싱 - 범위로 꺼내기
    print(플레이리스트[0:2]) # ['사랑노래', '축제노래']                      index 0 번부터 2-1까지 조회
    print(플레이리스트[1:])  # ['축제노래', '팝송', 'R&B', '힙합', '발라드'] index 1번부터 마지막까지
    print(플레이리스트[:2])  # ['사랑노래', '축제노래']                      index 0 번부터 2-1까지 조회
    print(플레이리스트[::2]) # ['사랑노래', '팝송', '힙합']                  index 0 번부터 2칸씩 건너뛰어 마지막까지 조회


    # 3. list comprehension 한줄 작성
    # 유튜브 조회수 목록에서 100만 이상만 필터
    조회수목록 = [500000, 1200000, 800000, 2000000, 3000000]

    백만이상리스트 = []
    for i in 조회수목록:
        if i >= 1000000:
            백만이상리스트.append(i)
    #              for문에 대한 결과를 [] 안에 담겠다.        수행할반복작업          반복작업에서 조회수가 1000000 인것만
    백만이상 = [             i                            for i in 조회수목록       if i >= 1000000]
    print(백만이상)

def 넷플릭스_시청시간():
    # 넷플릭스 콘텐츠별 시청시간 (분)
    시청시간목록 = [30, 120, 45, 200, 90, 150, 20, 180]

    # 일반 for문
    한시간이상리스트 = []
    for i in 시청시간목록:
        if i >= 60:
            한시간이상리스트.append(i)
    print(한시간이상리스트)
    # [120, 200, 90, 150, 180]

    # list comprehension 으로 바꾸기
    # [저장할값  for 변수 in 목록  if 조건]
    한시간이상 = [i  for i in 시청시간목록  if i >= 60]
    print(한시간이상)
    # [120, 200, 90, 150, 180]