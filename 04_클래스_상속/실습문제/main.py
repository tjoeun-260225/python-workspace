from 회원 import 회원
from 일반회원 import 일반회원
from VIP회원 import VIP회원
from 프리미엄회원 import 프리미엄회원
def main():
    print("========== 문제1,2: 기본 회원 ==========")
    A회원 = 회원("홍길동", 25, "hong@test.com")
    B회원 = 회원("김철수", 30, "chul@test.com")
    print(A회원.플랫폼)
    A회원.회원소개()
    B회원.회원소개()

    print("\n========== 문제3: 일반회원 (상속) ==========")
    C회원 = 일반회원("이영희", 22, "lee@test.com")
    C회원.회원소개()
    C회원.혜택안내()

    print("\n========== 문제4: VIP회원 (super + 오버라이딩) ==========")
    D회원 = VIP회원("박지훈", 35, "park@test.com", 500000)
    D회원.회원소개()          # 오버라이딩된 메서드 실행
    D회원.혜택안내()

    print("\n========== 문제5: 프리미엄회원 (다중상속) ==========")
    E회원 = 프리미엄회원("최수연", 28, "choi@test.com")
    E회원.회원소개()
    E회원.로그인()           # 로그인기능에서 상속
    E회원.알림보내기()           # 알림기능에서 상속
    E회원.혜택안내()

    print("\n========== 클래스변수 공유 확인 ==========")
    # 클래스변수는 모든 인스턴스가 공유한다
    회원.플랫폼 = "뉴플랫폼"
    print(A회원.플랫폼)      # 뉴플랫폼
    print(C회원.플랫폼)      # 뉴플랫폼

# main 함수 호출
main()