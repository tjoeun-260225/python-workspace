# 1. 클래스 선언
class 회원:

    # 10. 클래스변수 - 모든 회원이 공유하는 플랫폼 이름
    플랫폼 = "우리플랫폼"

    # 3. __init__ - 인스턴스 만들 때 자동 실행
    def __init__(self, 이름, 나이, 이메일):

        # 11. 인스턴스변수
        self.이름   = 이름
        self.나이   = 나이
        self.이메일 = 이메일

    # 5. 메서드
    def 회원소개(self):
        print(f"[{self.플랫폼}] {self.이름}님 / 나이: {self.나이} / 이메일: {self.이메일}")
