# 1. 클래스 선언
class 회원:

    # 10. 클래스변수 - 모든 회원이 공유하는 플랫폼 이름
    플랫폼 = "우리플랫폼"

    # 3. __init__ - 인스턴스 만들 때 자동 실행
    def __init__(self, 이름, 나이, 이메일):

        # 11. 인스턴스변수
        self.이름   = 이름
        self.나이   = 나이
        self.이메일 = 이메일

    # 5. 메서드
    def 회원소개(self):
        print(f"[{self.플랫폼}] {self.이름}님 / 나이: {self.나이} / 이메일: {self.이메일}")

# 2. 인스턴스 생성
A회원 = 회원("홍길동", 25, "hong@test.com")
B회원 = 회원("김철수",    30, "chul@test.com")

# 클래스변수 출력
print(A회원.플랫폼)     # 우리플랫폼
print(B회원.플랫폼)     # 우리플랫폼

# 메서드 호출
A회원.회원소개()
B회원.회원소개()

# 6. 상속 - () 안에 부모 클래스 작성
class 일반회원(회원):

    # 추가 메서드
    def 혜택안내(self):
        print(f"{self.이름}님의 혜택: 기본 적립 1%")

class VIP회원(회원):

    # 7. super() 로 부모 __init__ 실행 + 추가 인스턴스변수
    def __init__(self, 이름, 나이, 이메일, 한도):
        super().__init__(이름, 나이, 이메일)   # 부모 생성자 실행
        self.한도 = 한도                        # VIP 전용 추가 변수

    # 8. 오버라이딩 - 부모의 회원소개를 VIP 버전으로 재구성
    def 소개(self):
        print(f"[VIP] {self.이름}님 / 나이: {self.나이} / 한도: {self.한도}원")

    def 혜택안내(self):
        print(f"{self.이름}님의 혜택: VIP 적립 5% + 무료배송")


class 로그인기능:
    def 로그인(self):
        print(f"{self.이름}님이 로그인했습니다.")

class 알림기능:
    def 알림보내기(self):
        print(f"{self.이름}님께 이메일 알림을 보냈습니다.")

# 9. 다중상속 - () 안에 여러 부모 클래스 작성
#                  class 옆에 오는 소괄호는 매개변수가 아니라 상속받을 클래스 이름을 작성
#                  없는 클래스 명칭을 작성하면 안된다
class 프리미엄회원(회원, 로그인기능, 알림기능):

    def __init__(self, 이름, 나이, 이메일):
        super().__init__(이름, 나이, 이메일)

    def 혜택안내(self):
        print(f"{self.이름}님의 혜택: 프리미엄 적립 10% + 전용 CS")

def main():
    print("========== 문제1,2: 기본 회원 ==========")
    A회원 = 회원("홍길동", 25, "hong@test.com")
    B회원 = 회원("김철수", 30, "chul@test.com")
    print(A회원.플랫폼)
    A회원.회원소개()
    B회원.회원소개()

    print("\n========== 문제3: 일반회원 (상속) ==========")
    C회원 = 일반회원("이영희", 22, "lee@test.com")
    C회원.회원소개()
    C회원.혜택안내()

    print("\n========== 문제4: VIP회원 (super + 오버라이딩) ==========")
    D회원 = VIP회원("박지훈", 35, "park@test.com", 500000)
    D회원.회원소개()          # 오버라이딩된 메서드 실행
    D회원.혜택안내()

    print("\n========== 문제5: 프리미엄회원 (다중상속) ==========")
    E회원 = 프리미엄회원("최수연", 28, "choi@test.com")
    E회원.회원소개()
    E회원.로그인()           # 로그인기능에서 상속
    E회원.알림보내기()           # 알림기능에서 상속
    E회원.혜택안내()

    print("\n========== 클래스변수 공유 확인 ==========")
    # 클래스변수는 모든 인스턴스가 공유한다
    회원.플랫폼 = "뉴플랫폼"
    print(A회원.플랫폼)      # 뉴플랫폼
    print(C회원.플랫폼)      # 뉴플랫폼

# main 함수 호출
main()